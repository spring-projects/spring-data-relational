CREATE TABLE DUMMY_ENTITY
(
    ID   BIGINT GENERATED BY DEFAULT AS IDENTITY ( START WITH 1 ) PRIMARY KEY,
    NAME VARCHAR(100)
);
CREATE TABLE CHILD_ELEMENT
(
    ID           BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1) PRIMARY KEY,
    NAME         VARCHAR(100),
    DUMMY_ENTITY BIGINT
);
CREATE TABLE GRAND_CHILD_ELEMENT
(
    ID            BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1) PRIMARY KEY,
    CONTENT       VARCHAR(100),
    CHILD_ELEMENT BIGINT
);
